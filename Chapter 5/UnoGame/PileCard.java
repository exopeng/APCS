
public class PileCard {
	private String pileColor;
	private int pileValue;
	public PileCard(String pileColor, int pileValue) {
		this.pileColor = pileColor;
		this.pileValue = pileValue;
	}
	public String getPileColor() {
		return pileColor;
	}
	public void setPileColor(String pileColor) {
		this.pileColor = pileColor;
	}
	public int getPileValue() {
		return pileValue;
	}
	public void setPileValue(int pileValue) {
		this.pileValue = pileValue;
	}
	
	
}
